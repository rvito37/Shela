export function getPersonaPrompt(slug: string): string {
  const persona = PERSONA_PROMPTS.find((p) => p.slug === slug);
  if (!persona) {
    throw new Error(`Persona prompt not found for slug: ${slug}`);
  }
  return persona.systemPrompt;
}

export function buildGenerationPrompt(options: {
  topic: string;
  contentType: ContentType;
  additionalInstructions?: string;
}): string {
  const typeInstructions: Record<ContentType, string> = {
    article: `כתבי מאמר מלא (800-1500 מילים). כללי: כותרת חזקה, 3-5 תת-כותרות, פסקאות קצרות, סיכום.`,
    quick_take: `כתבי תגובה קצרה וחדה (200-400 מילים). תגובה לטרנד או אירוע אקטואלי. ישירה ותכליתית.`,
    list: `כתבי רשימה/ליסטיקל (500-800 מילים). כותרת עם מספר ("7 דרכים...", "5 דברים..."). כל פריט עם כותרת משנה והסבר קצר.`,
    qa: `כתבי בפורמט שאלות ותשובות. 4-6 שאלות נפוצות עם תשובות מפורטות ואישיות.`,
    column: `כתבי טור שבועי (600-1000 מילים). אישי, עם תובנה מרכזית אחת. פתיחה סיפורית, עומק באמצע, סיום עם מסר.`,
  };

  let prompt = `כתבי ${typeInstructions[options.contentType]}\n\nהנושא: ${options.topic}`;

  if (options.additionalInstructions) {
    prompt += `\n\nהנחיות נוספות: ${options.additionalInstructions}`;
  }

  prompt += `\n\nפורמט התשובה:
החזירי JSON בפורמט הבא (ללא markdown code block):
{
  "title": "הכותרת בעברית",
  "excerpt": "תקציר של 1-2 משפטים שמושך לקרוא",
  "content": "התוכן המלא ב-Markdown",
  "seo_title": "כותרת SEO (עד 60 תווים)",
  "seo_description": "תיאור SEO (עד 155 תווים)"
}`;

  return prompt;
}
